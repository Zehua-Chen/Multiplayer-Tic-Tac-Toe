namespace TicTacToe
{
    /// <summary>
    /// A point in the board of a tic tac toe game
    /// </summary>
    public struct Location
    {
        /// <summary>
        /// Y
        /// </summary>
        /// <value>y</value>
        public int Y { get; set; }
        
        /// <summary>
        /// X
        /// </summary>
        /// <value>x</value>
        public int X { get; set; }
    }
}